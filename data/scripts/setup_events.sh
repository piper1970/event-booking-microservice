#!/bin/bash

# Relies on EVENTS_ADMIN_DB_USER, EVENTS_ADMIN_DB_PASSWORD,
# EVENTS_DB_USER, and EVENTS_DB_PASSWORD environment variables

psql <<-END
DROP DATABASE IF EXISTS events;
CREATE DATABASE events
    WITH
    OWNER = postgres
    ENCODING = 'UTF8'
    LOCALE_PROVIDER = 'libc'
    CONNECTION LIMIT = -1
    IS_TEMPLATE = False;

-- Create admin role
DROP ROLE IF EXISTS $EVENTS_ADMIN_DB_USER;
CREATE ROLE $EVENTS_ADMIN_DB_USER WITH
    LOGIN
    NOSUPERUSER
    NOCREATEDB
    NOCREATEROLE
    INHERIT
    NOREPLICATION
    NOBYPASSRLS
    CONNECTION LIMIT -1
    ENCRYPTED PASSWORD '$EVENTS_ADMIN_DB_PASSWORD';

-- Grant permissions for admin role
GRANT ALL ON DATABASE events TO $EVENTS_ADMIN_DB_USER;

-- move to events db
\c events

-- setup schema
CREATE SCHEMA event_service;
GRANT ALL ON SCHEMA event_service to $EVENTS_ADMIN_DB_USER;
GRANT ALL ON ALL TABLES IN SCHEMA event_service to $EVENTS_ADMIN_DB_USER;
GRANT ALL ON ALL SEQUENCES IN SCHEMA event_service to $EVENTS_ADMIN_DB_USER;

-- Create regular user
DROP ROLE IF EXISTS $EVENTS_DB_USER;
CREATE ROLE $EVENTS_DB_USER WITH
    LOGIN
    NOSUPERUSER
    INHERIT
    NOCREATEDB
    NOCREATEROLE
    NOREPLICATION
    NOBYPASSRLS
    CONNECTION LIMIT -1
    ENCRYPTED PASSWORD '$EVENTS_DB_PASSWORD';

-- Grant permissions for regular user
GRANT CONNECT ON DATABASE events TO $EVENTS_DB_USER;
GRANT USAGE ON SCHEMA event_service to $EVENTS_DB_USER;
GRANT SELECT, INSERT, UPDATE, DELETE ON ALL TABLES IN SCHEMA event_service to $EVENTS_DB_USER;
GRANT USAGE, SELECT ON ALL SEQUENCES IN SCHEMA event_service to $EVENTS_DB_USER;
ALTER DEFAULT PRIVILEGES IN SCHEMA event_service FOR ROLE $EVENTS_ADMIN_DB_USER
    GRANT SELECT, UPDATE, DELETE, INSERT ON TABLES TO $EVENTS_DB_USER;
ALTER DEFAULT PRIVILEGES IN SCHEMA event_service FOR ROLE $EVENTS_ADMIN_DB_USER
    GRANT USAGE, SELECT ON SEQUENCES TO $EVENTS_DB_USER;

END